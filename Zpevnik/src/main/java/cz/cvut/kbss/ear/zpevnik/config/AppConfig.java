import org.springframework.security.crypto.password.PasswordEncoder;

/**
 *
 * @author Adam Å karda
 */
@Configuration
public class AppConfig {
    
    @Bean
    public PasswordEncoder passwordEncoder() {
        return new BCryptPasswordEncoder();
    }
}
